nome = input("Informe seu nome: ").title()
print(f"Seu nome é: {nome}")

texto = input("Informe um texto: ").capitalize()
print(f"Seu texto é: {texto}")

minuscula = input("Informe um texto em minúsculas: ").upper()
print(f"Seu texto em maiúsculas é: {minuscula}")

maiuscula = input("Informe um texto em maiúsculas: ").lower()
print(f"Seu texto em minúsculas é: {maiuscula}")

